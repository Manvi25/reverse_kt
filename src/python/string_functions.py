
str1 = "Hello World"
swapped = str1.swapcase()
print(swapped)


starts_with_hello = str1.startswith("Hello")
print(starts_with_hello)


split_str = str1.split(" ")
print(split_str)


whitespace_str = "   Hello World   "
stripped_str = whitespace_str.strip()
print(stripped_str)

lower_str = str1.lower()
print(lower_str)

upper_str = str1.upper()
print(upper_str)


replaced_str = str1.replace("World", "Python")
print(replaced_str)
